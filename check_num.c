#include <stdio.h>

// Input a number and check if it is greater than 50.

int main()
{
    int num;

    printf("Enter a Number: ");
    scanf("%d", &num);

    if (num > 50)
    {
        printf("Number is greater than 50");
    }
    else
    {
        printf("number is less than or equal to 50");
    }

   
}
